        <h1 class='blog-overview'> Chocolate Chip Cookies</h1>
        <br>
        <h2>Ingredients:</h2>
    <p>1 cup (2 sticks) unsalted butter, softened</p>
    <p>1 cup granulated sugar</p>
    <p>1 cup packed brown sugar</p>
    <p>2 large eggs</p>
    <p>1 teaspoon pure vanilla extract</p>
    <p>3 cups all-purpose flour</p>
    <p>1 teaspoon baking soda</p>
    <p>1/2 teaspoon baking powder</p>
    <p>1/2 teaspoon salt</p>
    <p>2 cups semi-sweet chocolate chips</p>

    <h2>Instructions:</h2>
    <br />
    <p>1. Preheat the oven to 375°F (190°C). Line a baking sheet with parchment paper or silicone baking mat.</p>
    <p>2. In a large mixing bowl, cream together the softened butter, granulated sugar, and brown sugar until smooth and creamy.</p>
    <p>3. Add the eggs one at a time, beating well after each addition. Stir in the vanilla extract.</p>
    <p>4. In a separate bowl, whisk together the all-purpose flour, baking soda, baking powder, and salt.</p>
    <p>5. Gradually add the dry ingredients to the wet ingredients, mixing until just combined. Do not overmix.</p>
    <p>6. Fold in the semi-sweet chocolate chips until they are evenly distributed in the dough.</p>
    <p>7. Drop rounded tablespoons of cookie dough onto the prepared baking sheet, leaving enough space between each cookie to allow for spreading.</p>
    <p>8. Bake the cookies in the preheated oven for 9 to 11 minutes or until the edges are golden brown.</p>
    <p>9. Remove the baking sheet from the oven and let the cookies cool for a few minutes before transferring them to a wire rack to cool completely.</p>
